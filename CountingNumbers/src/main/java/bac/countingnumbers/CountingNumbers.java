package bac.countingnumbers;

/**
 *
 * @author adam
 */
public class CountingNumbers {

    public static void main(String[] args) {
        
        int counter = 1; // initializing our variable
        
        while (counter <= 5)
        {
            System.out.println("Counting: " + counter);
            counter++;
        }
        
        System.out.println("...Done!");
    }
}
