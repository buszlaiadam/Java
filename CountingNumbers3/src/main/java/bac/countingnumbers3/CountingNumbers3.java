public class CountingNumbers3 {

    public static void main(String[] args) {
        
        int counter;
        
        for (counter = 1; counter <= 5; counter++)
        {
            System.out.println("Counting: " + counter);
        }
        
        System.out.println("...Done!");
    }
}
